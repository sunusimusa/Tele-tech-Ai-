const TelegramBot = require("node-telegram-bot-api");
const fetch = require("node-fetch");

// üîê Token
const TOKEN = process.env.TELEGRAM_BOT_TOKEN;

// üåç API URL na backend …óinka
const API_URL = "https://tele-tech-ai.onrender.com/generate";

// ‚úÖ START BOT (POLLING SAU ∆äAYA KAWAI)
const bot = new TelegramBot(TOKEN, { polling: true });

console.log("ü§ñ Telegram bot is running...");

// /start
bot.onText(/^\/start/, (msg) => {
  bot.sendMessage(
    msg.chat.id,
    "üëã Welcome to *Tele Image AI*\n\nüé® Turo rubutu (prompt) domin a ∆ôir∆ôiri hoto.",
    { parse_mode: "Markdown" }
  );
});

// Receive prompt
bot.on("message", async (msg) => {
  const chatId = msg.chat.id;
  const prompt = msg.text;

  if (!prompt || prompt.startsWith("/")) return;

  await bot.sendMessage(chatId, "üé® Generating image, please wait...");

  try {
    const res = await fetch(API_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        email: `telegram_${chatId}@user.com`,
        prompt
      })
    });

    const data = await res.json();

    if (data.error) {
      return bot.sendMessage(chatId, "‚ùå " + data.error);
    }

    await bot.sendPhoto(chatId, data.image, {
      caption: "‚úÖ Generated by Tele Image AI"
    });

  } catch (err) {
    console.error(err);
    bot.sendMessage(chatId, "‚ö†Ô∏è Server error, try again later");
  }
});
