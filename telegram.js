const TelegramBot = require("node-telegram-bot-api");
const fetch = require("node-fetch");

// ğŸ” Telegram Bot Token
const TOKEN = process.env.TELEGRAM_BOT_TOKEN;

// ğŸŒ API URL na Tele-tech-AI
const API_URL = "https://tele-tech-ai.onrender.com/generate";

// âœ… START BOT (POLLING ÆŠAYA KAWAI)
const bot = new TelegramBot(TOKEN, {
  polling: {
    interval: 300,
    autoStart: true
  }
});

// ğŸ‘‹ /start command
bot.onText(/\/start/, (msg) => {
  bot.sendMessage(
    msg.chat.id,
    "ğŸ‘‹ Welcome to Tele Image AI!\n\nğŸ¨ Send any text prompt to generate an image."
  );
});

// ğŸ¨ Receive prompt
bot.on("message", async (msg) => {
  const chatId = msg.chat.id;
  const prompt = msg.text;

  if (!prompt || prompt.startsWith("/")) return;

  await bot.sendMessage(chatId, "ğŸ¨ Generating image, please wait...");

  try {
    const res = await fetch(API_URL, {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({
        email: `telegram_${chatId}@user.com`,
        prompt
      })
    });

    const data = await res.json();

    if (data.error) {
      await bot.sendMessage(chatId, "âŒ " + data.error);
      return;
    }

    await bot.sendPhoto(chatId, data.image, {
      caption: "âœ… Generated by Tele Image AI"
    });

  } catch (err) {
    console.error(err);
    await bot.sendMessage(chatId, "âš ï¸ Server error, try again later");
  }
});

console.log("ğŸ¤– Telegram bot is running...");
